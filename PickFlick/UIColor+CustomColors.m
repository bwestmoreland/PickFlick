#import "UIColor+CustomColors.h"

@implementation UIColor (CustomColors)

+ (UIColor *)darkBlueColor
{
    return [UIColor colorWithRed: 46 / 255.0f
                           green: 101 / 255.0f
                            blue: 142 / 255.0f
                           alpha: 1.0];
}

+ (UIColor *)creamColor
{
    return [UIColor colorWithRed: 241 / 255.0f
                           green: 242 / 255.0f
                            blue: 226 / 255.0f
                           alpha: 1.0f];
}

@end
